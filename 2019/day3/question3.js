const move = (path, direction) => {
  const [dir, distance] = direction;
  const movement = {
    U: ([x, y]) => [x, ++y],
    D: ([x, y]) => [x, --y],
    R: ([x, y]) => [++x, y],
    L: ([x, y]) => [--x, y],
  };
  let i = distance;
  while (--i >= 0) {
    const lastPosition = path[0];
    path.unshift(movement[dir](lastPosition));
  }
  return path;
};
const parsePosition = (position) => {
  const direction = position.slice(0, 1);
  const distance = parseInt(position.slice(1));
  return [direction, distance];
};

